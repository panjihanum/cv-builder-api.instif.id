const LINK_ICON_KEYS = Object.keys(LINK_ICONS) as LinkIconKey[];

function isLinkIconKey(value: string): value is LinkIconKey {
  return value in LINK_ICONS;
}

function detectLinkIcon(url: string, label: string): LinkIconKey {
  const haystack = `${url} ${label}`.toLowerCase();
  for (const key of LINK_ICON_KEYS) {
    if (key === "globe" || key === "link") continue;
    if (LINK_ICONS[key].match.some((needle) => haystack.includes(needle))) {
      return key;
    }
  }
  if (haystack.includes("http") || haystack.includes("www.")) return "globe";
  return "link";
}

export function resolveLinkIcon(link: {
  icon?: string;
  url: string;
  label: string;
}): LinkIconKey {
  if (link.icon && isLinkIconKey(link.icon)) return link.icon;
  return detectLinkIcon(link.url, link.label);
}

export function linkIconSvg(
  key: LinkIconKey,
  size = 13,
  colored = false
): string {
  const def = LINK_ICONS[key] ?? LINK_ICONS.link;
  const fill = colored ? def.color : "currentColor";
  return `<svg viewBox="0 0 24 24" width="${size}" height="${size}" style="fill:${fill};flex-shrink:0;vertical-align:-2px" aria-hidden="true"><path d="${def.path}" /></svg>`;
}
